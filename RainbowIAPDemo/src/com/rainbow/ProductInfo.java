package com.rainbow;

public class ProductInfo
{
	private int id;
	private int pic;
	private int name;
	private int count;
	private int price;
	private int desc;
	
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public int getPic()
	{
		return pic;
	}
	public void setPic(int pic)
	{
		this.pic = pic;
	}
	public int getName()
	{
		return name;
	}
	public void setName(int name)
	{
		this.name = name;
	}
	public int getCount()
	{
		return count;
	}
	public void setCount(int count)
	{
		this.count = count;
	}
	public int getPrice()
	{
		return price;
	}
	public void setPrice(int price)
	{
		this.price = price;
	}
	public int getDesc()
	{
		return desc;
	}
	public void setDesc(int desc)
	{
		this.desc = desc;
	}
}
